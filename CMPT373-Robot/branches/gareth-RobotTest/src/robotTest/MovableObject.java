package robotTest;

//This is a generic MovableObject template. For now it only contains the x- and y-coordinates of the movable object.
public class MovableObject {
	
	private int x_pos;
	private int y_pos;
	private int direction;
	
	public MovableObject(int x, int y, int d){
		x_pos = x;
		y_pos = y;
		direction = d;
	}
	
	public int getX(){
		return x_pos;
	}
	
	public int getY(){
		return y_pos;
	}
	
	public void setPosition(int x, int y){
		x_pos = x;
		y_pos = y;
	}
	
	public void incrementPosition(int x, int y){
		x_pos = x_pos + x;
		y_pos = y_pos + y;
	}
}
