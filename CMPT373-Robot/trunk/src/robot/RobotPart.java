package robot;
import board.*;

/**
 * RobotPart Class
 * <P> This is a generic RobotPart template. All Robot upgrades (ie. motors, cameras, guns, canadarms) will inherit from RobotPart. 
 */
public class RobotPart {
	private int part_price;
	private int part_weight;
	
	/**
	 * Constructor.
	 * @param part_price The price of the part.
	 * @param part_weight The weight of the part.
	 */
	public RobotPart(int price, int weight){
		part_price = price;
		part_weight = weight;
	}
}
